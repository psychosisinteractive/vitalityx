uint32_t getScancode()
{
    uint32_t flag = inb(0x64);
    while(!(flag & 1))
        flag = inb(0x64);
    return inb(0x60);
}

char getchar()
{
    return scancode[getScancode()];
}

char* getstr() {
    tty_putstr(">",DarkGray,Black);
    char *strm;
    char gc = getchar();
    while(gc != '\n') {
        tty_putstr(gc,White,Black);
        *strm++ = gc;
        gc = getchar();
    }
    return strm;
}

